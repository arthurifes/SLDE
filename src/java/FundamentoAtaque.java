class FundamentoAtaque {
    
    private int id;
    
    private Integer gol, assistencia, finalizacaoNaTrave, finalizacaoDefendida, finalizacaoFora, faltaSofrida,
                    penaltiPerdido, impedimento, passeErrado;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }


    

    public Integer getGol() {
        return gol;
    }

    public void setGol(Integer gol) {
        this.gol = gol;
    }

    public Integer getAssistencia() {
        return assistencia;
    }

    public void setAssistencia(Integer assistencia) {
        this.assistencia = assistencia;
    }

    public Integer getFinalizacaoNaTrave() {
        return finalizacaoNaTrave;
    }

    public void setFinalizacaoNaTrave(Integer finalizacaoNaTrave) {
        this.finalizacaoNaTrave = finalizacaoNaTrave;
    }

    public Integer getFinalizacaoDefendida() {
        return finalizacaoDefendida;
    }

    public void setFinalizacaoDefendida(Integer finalizacaoDefendida) {
        this.finalizacaoDefendida = finalizacaoDefendida;
    }

    public Integer getFinalizacaoFora() {
        return finalizacaoFora;
    }

    public void setFinalizacaoFora(Integer finalizacaoFora) {
        this.finalizacaoFora = finalizacaoFora;
    }

    public Integer getFaltaSofrida() {
        return faltaSofrida;
    }

    public void setFaltaSofrida(Integer faltaSofrida) {
        this.faltaSofrida = faltaSofrida;
    }

    public Integer getPenaltiPerdido() {
        return penaltiPerdido;
    }

    public void setPenaltiPerdido(Integer penaltiPerdido) {
        this.penaltiPerdido = penaltiPerdido;
    }

    public Integer getImpedimento() {
        return impedimento;
    }

    public void setImpedimento(Integer impedimento) {
        this.impedimento = impedimento;
    }

    public Integer getPasseErrado() {
        return passeErrado;
    }

    public void setPasseErrado(Integer passeErrado) {
        this.passeErrado = passeErrado;
    }
    
}
